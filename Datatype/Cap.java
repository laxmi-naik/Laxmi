class Cap{
	String type;
	double price;
	String colour;
	Cap(String typeLocal,double priceLocal,String colourLocal){
		type=typeLocal;
		price=priceLocal;
		colour=colourLocal;
		System.out.println("cap type:"+type);
		System.out.println("cap price:"+price);
		System.out.println("cap colour:"+colour);
	}
}